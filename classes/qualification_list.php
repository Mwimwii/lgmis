	public function emptyRow()
	{
		global $CurrentForm;
		if ($CurrentForm->hasValue("x_QualificationName") && $CurrentForm->hasValue("o_QualificationName") && $this->QualificationName->CurrentValue != $this->QualificationName->OldValue)
