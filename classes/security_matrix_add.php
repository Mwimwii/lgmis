	// Set up master/detail based on QueryString
	protected function setupMasterParms()
	{
		$validMaster = FALSE;

		// Get the keys for master table
		if (($master = Get(Config("TABLE_SHOW_MASTER"), Get(Config("TABLE_MASTER")))) !== NULL) {
			$masterTblVar = $master;
			if ($masterTblVar == "") {
				$validMaster = TRUE;
				$this->DbMasterFilter = "";
				$this->DbDetailFilter = "";
			}
			if ($masterTblVar == "musers") {
				$validMaster = TRUE;
				if (($parm = Get("fk_UserCode", Get("UserCode"))) !== NULL) {
					$GLOBALS["musers"]->UserCode->setQueryStringValue($parm);
					$this->UserCode->setQueryStringValue($GLOBALS["musers"]->UserCode->QueryStringValue);
					$this->UserCode->setSessionValue($this->UserCode->QueryStringValue);
					if (!is_numeric($GLOBALS["musers"]->UserCode->QueryStringValue))
						$validMaster = FALSE;
				} else {
					$validMaster = FALSE;
				}
			}
		} elseif (($master = Post(Config("TABLE_SHOW_MASTER"), Post(Config("TABLE_MASTER")))) !== NULL) {
			$masterTblVar = $master;
			if ($masterTblVar == "") {
				$validMaster = TRUE;
				$this->DbMasterFilter = "";
				$this->DbDetailFilter = "";
			}
			if ($masterTblVar == "musers") {
				$validMaster = TRUE;
				if (($parm = Post("fk_UserCode", Post("UserCode"))) !== NULL) {
					$GLOBALS["musers"]->UserCode->setFormValue($parm);
					$this->UserCode->setFormValue($GLOBALS["musers"]->UserCode->FormValue);
					$this->UserCode->setSessionValue($this->UserCode->FormValue);
					if (!is_numeric($GLOBALS["musers"]->UserCode->FormValue))
						$validMaster = FALSE;
				} else {
					$validMaster = FALSE;
				}
			}
		}
		if ($validMaster) {

			// Save current master table
			$this->setCurrentMasterTable($masterTblVar);

			// Reset start record counter (new master key)
			if (!$this->isAddOrEdit()) {
				$this->StartRecord = 1;
				$this->setStartRecordNumber($this->StartRecord);
			}

			// Clear previous master key from Session
			if ($masterTblVar != "musers") {
				if ($this->UserCode->CurrentValue == "")
					$this->UserCode->setSessionValue("");
			}
		}
		$this->DbMasterFilter = $this->getMasterFilter(); // Get master filter
		$this->DbDetailFilter = $this->getDetailFilter(); // Get detail filter
	}

